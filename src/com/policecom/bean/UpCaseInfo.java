package com.policecom.bean;

public class UpCaseInfo {
	private String casePt="";
	private String ptCity="";
	private String ptDis="";
	private String ptStreet="";
	
	public String getCasePt() {
		return this.casePt;
	}
	public void setCasePt(String casePt) {
		this.casePt=casePt;
	}
	
	public String getPtCity() {
		return this.ptCity;
	}
	public void setPtCity(String ptCity) {
		this.ptCity=ptCity;
	}
	
	public String getPtDis() {
		return this.ptDis;
	}
	public void setPtDis(String ptDis) {
		this.ptDis=ptDis;
	}
	
	public String getPtStreet() {
		return this.ptStreet;
	}
	public void setPtStreet(String ptStreet) {
		this.ptStreet=ptStreet;
	}
}
